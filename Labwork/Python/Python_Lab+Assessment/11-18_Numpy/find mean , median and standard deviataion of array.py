import numpy as np

# Input
arr = np.array([10, 20, 30, 40, 50])

# Output
mean = np.mean(arr)
median = np.median(arr)
std_dev = np.std(arr)

print("Mean:", mean)
print("Median:", median)
print("Standard Deviation:", std_dev)
# Mean: 30.0
# Median: 30.0
# Standard Deviation: 14.142135623730951